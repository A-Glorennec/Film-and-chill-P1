const section = document.querySelector(".container-categories")

cards.map((card) => {

    const article = document.createElement("article")
    article.classList.add("cards-categories")

    const title = document.createElement("h1")
    title.classList.add("categories-title")
    title.textContent = card.categorie

    article.appendChild(title)

    const movieContainer = document.createElement("div");
    movieContainer.classList.add("movie-container")
    article.appendChild(movieContainer)

    card.items.map((item) => {

        const img = document.createElement("img")
        img.classList.add("categories-img")
        img.src = item.image
        img.alt = item.alt

        movieContainer.appendChild(img);

        console.log(img.src)

    })
    section.appendChild(article)
})